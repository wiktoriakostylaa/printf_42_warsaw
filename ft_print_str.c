#include "ft_printf.h"

int	ft_print_str(char *s)
{
	int	i;

	i = 0;
	if (!s)
		s = "(null)";
	ft_putstr_fd(s, 1);
	while (s[i] != '\0')
		i++;
	return (i);
}
